public class Switch01 {


    public static void main(String[] args) {
        int day = 1;

        switch (day) //4
        {
            case 1 :
                System.out.println("Sunday");
                System.out.println("uii");
                break;
            case 2 :
                System.out.println("Monday");
                break;
            case 3 :
                System.out.println("Tuesday");
                break;
            default:
                System.out.println("No day");
        }




    }
}
