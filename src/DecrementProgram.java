public class DecrementProgram {
    public static void main(String[] args) {

        //Pre-decrement
        int a = 10;
        --a;
        System.out.println(a); // 9

        int b= 10;
        System.out.println(--b); //9


        //post decrement
        int c = 10;
        c--;
        System.out.println(c); //9

        int d = 10;
        System.out.println(d--); //10


    }
}
