public class IncrementProgram {

    public static void main(String[] args) {

        //pre- increment

        int a = 10;
        ++a;
        System.out.println(a); //11

        int b=10;
        System.out.println(++b); //11


        //post-increment
        int c = 10;
        c++;
        System.out.println(c); //11


        int d = 10;
        System.out.println(d++); //10
        System.out.println(d); //11






    }
}
